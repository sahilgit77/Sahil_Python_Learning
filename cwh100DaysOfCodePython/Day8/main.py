# Exercise 1 - Create a Calculator Solution
# Solution

print(15+7) # + -> Addition
print(15-7) # - -> Subtraction
print(15*7) # * -> Multiplication
print(15/7) # / -> Division -> it gives o/p in float
print(15//7) # / -> Floor Division -> use '//' to get output in integer, like c/c++/java
print(15%7) # % -> Modulo -> to get Remainder
print(3**4) # % -> Exponential -> 2**3 = 2*2*2, 3**4 = 3*3*3*3
